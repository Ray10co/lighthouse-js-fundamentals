const ingredients = ["eggs", "milk", "flour", "sugar", "baking soda", 
"baking powder", "chocolate chips", "bananas"];

let i = 0;
while (i < ingredients.length) {
  console.log(ingredients[i]);
  i ++;
};

 
for(let b = 0; b < ingredients.length; b++) {
  console.log(ingredients[b]);
}

for (let e = ingredients.length; e >= 0; e--) {
  console.log(ingredients[e]);
}


